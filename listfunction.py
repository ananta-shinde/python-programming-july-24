
numbers = []

# adds value into list at last
numbers.append(20)
numbers.append(40)

# deletes last value from list
numbers.pop()

numbers.append(50)

# delete specified value from list
numbers.remove(20)

# update list by index
numbers[0] = 75


#print list at once
print(numbers)

#print exactly one value by index
print(numbers[0])

# delete all values from list
numbers.clear()

